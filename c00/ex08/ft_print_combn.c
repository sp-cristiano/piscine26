#include <unistd.h>

void	ft_print_combn(int n)
{
	int	t[10];
	int	i;

	i = -1;
	while (++i < n)
		t[i] = i;
	while (t[0] <= 10 - n && n > 0 && n < 10)
	{
		i = -1;
		while (++i < n)
		{
			t[i] += '0';
			write(1, &t[i], 1);
			t[i] -= '0';
		}
		if (t[0] == 10 - n)
			break ;
		write(1, ", ", 2);
		i = n;
		while (--i >= 0 && t[i] == 10 - n + i)
			;
		t[i]++;
		while (++i < n)
			t[i] = t[i - 1] + 1;
	}
}
